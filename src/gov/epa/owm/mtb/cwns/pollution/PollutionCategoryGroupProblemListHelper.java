package gov.epa.owm.mtb.cwns.pollution;

public class PollutionCategoryGroupProblemListHelper {

	long pollutionCategoryGroupId = -999;
	
	String pollutionCategoryGroupName = "";		
	
	long pollutionProblemId = -999;
	
	String pollutionProblemName = "";

	/**
	 * @return the pollutionCategoryGroupId
	 */
	public long getPollutionCategoryGroupId() {
		return pollutionCategoryGroupId;
	}

	/**
	 * @param pollutionCategoryGroupId the pollutionCategoryGroupId to set
	 */
	public void setPollutionCategoryGroupId(long pollutionCategoryGroupId) {
		this.pollutionCategoryGroupId = pollutionCategoryGroupId;
	}

	/**
	 * @return the pollutionCategoryGroupName
	 */
	public String getPollutionCategoryGroupName() {
		return pollutionCategoryGroupName;
	}

	/**
	 * @param pollutionCategoryGroupName the pollutionCategoryGroupName to set
	 */
	public void setPollutionCategoryGroupName(String pollutionCategoryGroupName) {
		this.pollutionCategoryGroupName = pollutionCategoryGroupName;
	}

	/**
	 * @return the pollutionProblemId
	 */
	public long getPollutionProblemId() {
		return pollutionProblemId;
	}

	/**
	 * @param pollutionProblemId the pollutionProblemId to set
	 */
	public void setPollutionProblemId(long pollutionProblemId) {
		this.pollutionProblemId = pollutionProblemId;
	}

	/**
	 * @return the pollutionProblemName
	 */
	public String getPollutionProblemName() {
		return pollutionProblemName;
	}

	/**
	 * @param pollutionProblemName the pollutionProblemName to set
	 */
	public void setPollutionProblemName(String pollutionProblemName) {
		this.pollutionProblemName = pollutionProblemName;
	}


}